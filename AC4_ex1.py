# Recebe n
n=int(input())

# o primeiro número a ser impresso é o 3, como pode ser visto nos exemplos
# dados no enunciado, então k inicializa em 3.
k = 3
while k <= n:       # Executa o while enquanto k menor ou igual a n
    print(k)
    k += 3          # Incrementa o k de 3 a cada passo.
